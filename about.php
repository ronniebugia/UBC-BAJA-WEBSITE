            <p>SAE Baja is an annual student design competition put on by the Society of Automative Engineers (SAE). The competition takes place over three days where each team's vehicle is judged on a series of static and dynamic events. The static events include cost and design presentations as well as vehicle inspection. The presentations are intended to test students' ability to justify their design choices and the feasibility of the design on a commercial market. Once passed inspection the vehicles compete in a hill climb, acceleration test, maneuverability test and a 4 hour endurance race.</p>
